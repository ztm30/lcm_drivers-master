static __inline void SPI_3W_SET_CMD(unsigned char data)
{
    unsigned int i;

    SET_LSCK_HIGH;
    UDELAY(10);
    SET_LSCE_LOW;
    UDELAY(1);
    SET_LSCK_LOW;
    SET_LSCK_LOW;
    SET_LSDA_LOW;
    SET_LSCK_LOW;
    SET_LSCK_LOW;
    SET_LSCK_HIGH;
    UDELAY(1);

    for (i = 0; i < 8; i++ )
    {
        SET_LSCK_LOW;
        if (data & 0x80) {
            SET_LSDA_HIGH;
        } else {
            SET_LSDA_LOW;
        }
        UDELAY(1);
        SET_LSCK_HIGH;
        UDELAY(1);
        data <<= 1;
    }
    SET_LSCE_HIGH;
}

